  @override
  String clientDeleteBlockedBody(
    int siteCount,
    int proofCount,
    bool hasActiveSession,
  ) {
    final parts = <String>[];
    if (siteCount > 0) {
      parts.add(
        siteCount == 1 ? '1 linked site' : '$siteCount linked sites',
      );
    }
    if (proofCount > 0) {
      parts.add(
        proofCount == 1 ? '1 linked proof' : '$proofCount linked proofs',
      );
    }
    if (hasActiveSession) {
      parts.add('an active session uses this client');
    }
    final detail = parts.isEmpty ? 'dependencies' : parts.join(', ');
    return 'Deletion is blocked by your current policy because this client still has: $detail. Change the policy in Settings or free this client first.';
  }

