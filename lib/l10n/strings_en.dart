    if (proofCount > 0) {
      lines.add(
        proofCount == 1
            ? '• 1 proof will become unclassified.'
            : '• $proofCount proofs will become unclassified.',
      );
    }
    if (hasActiveSession) {
      lines.add('• The active session will be ended.');
    }
    return lines.join('\n');
  }
