  @override
  String clientDeleteBlockedBody(
    int siteCount,
    int proofCount,
    bool hasActiveSession,
  ) {
    final parts = <String>[];
    if (siteCount > 0) {
      parts.add(
        siteCount == 1 ? '1 site lie' : '$siteCount sites lies',
      );
    }
    if (proofCount > 0) {
      parts.add(
        proofCount == 1 ? '1 preuve liee' : '$proofCount preuves liees',
      );
    }
    if (hasActiveSession) {
      parts.add('une session active utilise ce client');
    }
    final detail = parts.isEmpty ? 'des elements lies' : parts.join(', ');
    return 'La suppression est refusee par votre politique actuelle car ce client a encore : $detail. Modifiez la politique dans les reglages ou liberez ce client d\'abord.';
  }

  @override
  String clientDeleteImpactLines(
    int siteCount,
    int proofCount,
    bool hasActiveSession,
  ) {
    final lines = <String>[];
    if (siteCount > 0) {
      lines.add(
        siteCount == 1
            ? '• 1 site sera supprime.'
            : '• $siteCount sites seront supprimes.',
      );
    }
    if (proofCount > 0) {
      lines.add(
        proofCount == 1
            ? '• 1 preuve passera en non classee.'
            : '• $proofCount preuves passeront en non classees.',
      );
    }
    if (hasActiveSession) {
      lines.add('• La session active sera terminee.');
    }
    return lines.join('\n');
  }
