  // Grouped PDF — multi-proof report
  // ---------------------------------------------------------------------------
  static bool _groupedPdfInFlight = false;

  static Future<File> writeGroupedPdfToTemp({
    required List<Proof> proofs,
    required List<ProofPdfLabels> labelsList,
    required AppLocalizations l10n,
    PdfAppearancePreferences appearance = PdfAppearancePreferences.defaults,
    PdfMetadataExportMask metadataMask = PdfMetadataExportMask.all,
  }) async {
    if (_groupedPdfInFlight) {
      throw StateError('Grouped PDF generation already in progress');
    }
    _groupedPdfInFlight = true;
    try {
      final bytes = await buildGroupedPdfBytes(
        proofs: proofs,
        labelsList: labelsList,
        l10n: l10n,
        appearance: appearance,
        metadataMask: metadataMask,
      );
      final dir = await getTemporaryDirectory();
      final ts = DateTime.now().millisecondsSinceEpoch;
      final file = File('${dir.path}/stampsight_grouped_$ts.pdf');
      await file.writeAsBytes(bytes);
      return file;
    } finally {
      _groupedPdfInFlight = false;
    }
  }

  static const int _groupedImageMaxDim = 600;

  static img.Image _resizeForGrouped(img.Image src) {
    if (src.width <= _groupedImageMaxDim &&
        src.height <= _groupedImageMaxDim) {
      return src;
    }
    final landscape = src.width >= src.height;
    return img.copyResize(
      src,
      width: landscape ? _groupedImageMaxDim : null,
      height: landscape ? null : _groupedImageMaxDim,
      interpolation: img.Interpolation.average,
    );
  }

  static Future<Uint8List?> _loadImageForGrouped(String filePath) async {
    try {
      final f = File(filePath);
      if (!f.existsSync()) return null;
      final bytes = await f.readAsBytes();
      final decoded = img.decodeImage(bytes);
      if (decoded == null) return null;
      final resized = _resizeForGrouped(decoded);
      return Uint8List.fromList(img.encodePng(resized));
    } catch (_) {
      return null;
    }
  }

  static Future<Uint8List> buildGroupedPdfBytes({
    required List<Proof> proofs,
    required List<ProofPdfLabels> labelsList,
    required AppLocalizations l10n,
    PdfAppearancePreferences appearance = PdfAppearancePreferences.defaults,
    PdfMetadataExportMask metadataMask = PdfMetadataExportMask.all,
  }) async {
    final font = await _loadPdfUnicodeFont();
    String t(String s) => _pdfSafeText(s);

    final now = DateTime.now();
    final exportDate = _fmtDt(now);
    final premium = appearance.reportStyle == PdfReportStyle.premium;

    // Styles
    final sBrand = pw.TextStyle(
      font: font,
      fontSize: premium ? 10.5 : 8.5,
      fontWeight: pw.FontWeight.bold,
      color: _bleuNuit,
      letterSpacing: premium ? 3.5 : 2,
    );
    final sCoverTitle = pw.TextStyle(
      font: font,
      fontSize: premium ? 22 : 18,
      fontWeight: pw.FontWeight.bold,
      color: _bleuNuit,
    );
    final sCoverSub = pw.TextStyle(
      font: font,
      fontSize: premium ? 10 : 9,
      color: _grisLabel,
    );
    final sSec = pw.TextStyle(
      font: font,
      fontSize: premium ? 8.5 : 8,
      fontWeight: pw.FontWeight.bold,
      color: premium ? _bleuNuit : _grisFonce,
      letterSpacing: premium ? 1.5 : 0.8,
    );
    final sLbl = pw.TextStyle(font: font, fontSize: 9, color: _grisLabel);
    final sVal = pw.TextStyle(
      font: font, fontSize: 9.5,
      fontWeight: pw.FontWeight.bold, color: _grisFonce,
    );
    final sBody = pw.TextStyle(font: font, fontSize: 9.5, color: _grisFonce);
    final sDiscrete = pw.TextStyle(
      font: font, fontSize: 8.5, color: _grisLabel,
    );
    final sMeta = pw.TextStyle(font: font, fontSize: 7.5, color: _grisLabel);
    final sFtBrand = pw.TextStyle(
      font: font, fontSize: 8.5,
      fontWeight: pw.FontWeight.bold, color: _bleuNuit,
    );
    final sFtMeta = pw.TextStyle(font: font, fontSize: 7.5, color: _grisClair);
    final sFtLegal = pw.TextStyle(font: font, fontSize: 7, color: _grisClair);

    pw.Widget thinLine() => pw.Container(
          width: double.infinity, height: 0.5, color: _separateur,
        );

    pw.Widget kv(String label, String value,
            {pw.TextStyle? vs, double lw = 120}) =>
        pw.Padding(
          padding: const pw.EdgeInsets.only(bottom: 3),
          child: pw.Row(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              pw.SizedBox(width: lw, child: pw.Text(t(label), style: sLbl)),
              pw.Expanded(child: pw.Text(t(value), style: vs ?? sVal)),
            ],
          ),
        );

    pw.Widget accentCard({required pw.Widget child}) => premium
        ? pw.Container(
            decoration: pw.BoxDecoration(
              color: _fondClair,
              border: pw.Border(
                left: pw.BorderSide(color: _bleuNuit, width: 2.5),
              ),
            ),
            padding:
                const pw.EdgeInsets.symmetric(horizontal: 12, vertical: 8),
            child: child,
          )
        : pw.Container(
            decoration: pw.BoxDecoration(
              color: PdfColors.white,
              border: pw.Border.all(color: _separateur, width: 0.75),
            ),
            padding:
                const pw.EdgeInsets.symmetric(horizontal: 10, vertical: 7),
            child: child,
          );

    pw.Widget secTitle(String title) => premium
        ? pw.Column(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              pw.Text(t(title).toUpperCase(), style: sSec),
              pw.SizedBox(height: 3),
              pw.Container(width: 28, height: 1.5, color: _bleuNuit),
            ],
          )
        : pw.Padding(
            padding: const pw.EdgeInsets.only(bottom: 2),
            child: pw.Text(t(title).toUpperCase(), style: sSec),
          );

    // Build document
    final doc = pw.Document(
      title: t(l10n.pdfGroupedCoverTitle),
      author: 'StampSight',
    );
    final pdfTheme = pw.ThemeData.withFont(
      base: font, bold: font, italic: font, boldItalic: font,
    );

    // Cover page widgets
    final coverWidgets = <pw.Widget>[
      pw.Row(
        mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
        crossAxisAlignment: pw.CrossAxisAlignment.end,
        children: [
          pw.Text(
            appearance.showBranding ? 'STAMPSIGHT' : '',
            style: sBrand,
          ),
          pw.Text(exportDate, style: sMeta),
        ],
      ),
      pw.SizedBox(height: premium ? 5 : 4),
      pw.Container(
        width: double.infinity,
        height: premium ? 2 : 1,
        color: _bleuNuit,
      ),
      pw.SizedBox(height: premium ? 20 : 14),
      pw.Text(t(l10n.pdfGroupedCoverTitle), style: sCoverTitle),
      pw.SizedBox(height: 4),
      pw.Text(t(l10n.pdfGroupedCoverSubtitle), style: sCoverSub),
      pw.SizedBox(height: 16),
      thinLine(),
      pw.SizedBox(height: 12),
      accentCard(
        child: pw.Column(
          crossAxisAlignment: pw.CrossAxisAlignment.start,
          children: [
            kv(l10n.pdfGroupedProofsIncluded, '${proofs.length}'),
            kv(l10n.pdfGroupedExportDate, exportDate),
          ],
        ),
      ),
      pw.SizedBox(height: 16),
      thinLine(),
      pw.SizedBox(height: 16),
    ];

    pw.Widget framedGroupedShot(Uint8List bytes, double h) => pw.Container(
          decoration: pw.BoxDecoration(
            border: pw.Border.all(color: _separateur, width: 0.5),
          ),
          padding: const pw.EdgeInsets.all(2),
          child: pw.Image(
            pw.MemoryImage(bytes),
            height: h,
            fit: pw.BoxFit.contain,
          ),
        );

    // Each proof section
    final proofSections = <pw.Widget>[];
    for (var i = 0; i < proofs.length; i++) {
      final proof = proofs[i];
      final labels = labelsList[i];
      final hasNote =
          proof.description != null && proof.description!.isNotEmpty;
      final refId = proofReferenceLabelFromProofId(proof.id);
      final capturedDate = '${t(labels.dateValue)}  ${t(labels.timeValue)}';

      Uint8List? mainPng;
      if (proof.refersToLocalImageFile) {
        mainPng = await _loadImageForGrouped(proof.imagePath);
      }
      if (mainPng == null) {
        for (final path in [
          ProofCategoryAsset.placeholderFor(proof.proofType),
          ProofCategoryAsset.homeMirrorFor(proof.proofType),
        ]) {
          mainPng = await _assetAsPngBytes(path);
          if (mainPng != null) break;
        }
      }

      final sectionImages = <Uint8List>[];
      if (mainPng != null) sectionImages.add(mainPng);
      if (appearance.includeAllPhotos) {
        for (final extraPath in proof.additionalImagePaths) {
          final ex = await _loadImageForGrouped(extraPath);
          if (ex != null) sectionImages.add(ex);
        }
      }

      final hasClient = labels.clientValue != '-';
      final hasSite = labels.siteMissionValue != '-';
      final showClientInPdf = hasClient && metadataMask.includeClient;
      final showSiteInPdf = hasSite && metadataMask.includeSite;
      final showNoteInPdf = hasNote && metadataMask.includeNote;

      final titleStyle = pw.TextStyle(
        font: font,
        fontSize: premium ? 14 : 12,
        fontWeight: pw.FontWeight.bold,
        color: _bleuNuit,
      );
      final mainImgH = premium ? 240.0 : 200.0;
      final pairH = premium ? 200.0 : 170.0;
      final thumbH = 120.0;

      final imageWidgets = <pw.Widget>[];
      if (sectionImages.length == 1) {
        imageWidgets.add(pw.Center(child: framedGroupedShot(sectionImages[0], mainImgH)));
      } else if (sectionImages.length == 2) {
        imageWidgets.add(
          pw.Row(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              pw.Expanded(child: framedGroupedShot(sectionImages[0], pairH)),
              pw.SizedBox(width: 8),
              pw.Expanded(child: framedGroupedShot(sectionImages[1], pairH)),
            ],
          ),
        );
      } else if (sectionImages.length >= 3) {
        imageWidgets.add(
          pw.Center(child: framedGroupedShot(sectionImages[0], mainImgH * 0.92)),
        );
        imageWidgets.add(pw.SizedBox(height: 8));
        for (var j = 1; j < sectionImages.length; j += 2) {
          final hasSecond = j + 1 < sectionImages.length;
          imageWidgets.add(
            pw.Row(
              crossAxisAlignment: pw.CrossAxisAlignment.start,
              children: [
                pw.Expanded(child: framedGroupedShot(sectionImages[j], thumbH)),
                pw.SizedBox(width: 8),
                if (hasSecond)
                  pw.Expanded(
                      child: framedGroupedShot(sectionImages[j + 1], thumbH))
                else
                  pw.Expanded(child: pw.SizedBox()),
              ],
            ),
          );
          if (j + 2 < sectionImages.length) {
            imageWidgets.add(pw.SizedBox(height: 6));
          }
        }
      }

      proofSections.addAll([
        secTitle('${l10n.pdfGroupedProofSection} ${i + 1} / ${proofs.length}'),
        pw.SizedBox(height: 6),
        pw.Text(t(labels.docTitle), style: titleStyle),
        pw.SizedBox(height: 4),
        pw.Text('$refId  -  $capturedDate', style: sDiscrete),
        pw.SizedBox(height: 8),
        ...imageWidgets,
        pw.SizedBox(height: 6),
        accentCard(
          child: pw.Column(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              kv(labels.typeLabel, labels.typeValue),
              if (appearance.showDetailedLocation) ...[
                kv(labels.addressLabel, labels.addressValue),
                kv(labels.coordinatesLabel, labels.coordinatesValue,
                    vs: sDiscrete),
              ],
              if (showClientInPdf)
                kv(labels.clientLabel, labels.clientValue),
              if (showSiteInPdf)
                kv(labels.siteMissionLabel, labels.siteMissionValue),
            ],
          ),
        ),
        if (showNoteInPdf) ...[
          pw.SizedBox(height: 4),
          pw.Container(
            decoration: pw.BoxDecoration(
              border: pw.Border(
                left: pw.BorderSide(color: _grisLabel, width: 2),
              ),
            ),
            padding:
                const pw.EdgeInsets.only(left: 10, top: 2, bottom: 2),
            child: pw.Text(t(labels.noteValue), style: sBody),
          ),
        ],
        pw.SizedBox(height: 12),
        if (i < proofs.length - 1) ...[
          thinLine(),
          pw.SizedBox(height: 12),
        ],
      ]);
    }

    final totalPhotographs = proofs.fold<int>(
      0,
      (sum, p) =>
          sum +
          (appearance.includeAllPhotos ? p.totalImageCount : 1),
    );

    // Synthèse avant pied de page
    final closingWidgets = <pw.Widget>[
      if (appearance.showCredibilityBlock) ...[
        secTitle(l10n.pdfGroupedClosingTitle),
        pw.SizedBox(height: 6),
        accentCard(
          child: pw.Column(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              kv(l10n.pdfGroupedTotalPhotographs, '$totalPhotographs'),
              pw.SizedBox(height: 6),
              pw.Text(t(l10n.pdfDataIncludedDescription), style: sBody),
              pw.SizedBox(height: 6),
              pw.Text(t(l10n.pdfDocumentScopeNote), style: sDiscrete),
            ],
          ),
        ),
        pw.SizedBox(height: 10),
      ],
    ];

    // Footer widgets
    final footerWidgets = <pw.Widget>[
      pw.Container(width: double.infinity, height: 1.5, color: _bleuNuit),
      pw.SizedBox(height: 5),
      pw.Row(
        mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
        children: [
          pw.Text(
            appearance.showBranding ? 'StampSight' : '',
            style: sFtBrand,
          ),
          pw.Text(t(l10n.pdfGroupedCoverTitle), style: sFtMeta),
        ],
      ),
      pw.SizedBox(height: 2),
      pw.Text(exportDate, style: sFtMeta),
      if (appearance.showCredibilityBlock) ...[
        pw.SizedBox(height: 3),
        pw.Text(t(l10n.pdfAutoGenerated), style: sFtLegal),
      ],
    ];

    doc.addPage(
      pw.MultiPage(
        pageTheme: pw.PageTheme(
          margin: pw.EdgeInsets.symmetric(
            horizontal: premium ? 36 : 32,
            vertical: premium ? 24 : 20,
          ),
          theme: pdfTheme,
        ),
        build: (context) => [
          ...coverWidgets,
          ...proofSections,
          ...closingWidgets,
          ...footerWidgets,
        ],
      ),
    );

    try {
      return await doc.save();
    } catch (e, stack) {
      Error.throwWithStackTrace(
          StateError('Grouped PDF save failed: $e'), stack);
    }
  }
