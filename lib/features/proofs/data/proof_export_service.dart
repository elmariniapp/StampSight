  static Future<File> writePdfToTemp(
    Proof proof,
    ProofPdfLabels labels, {
    PdfAppearancePreferences appearance = PdfAppearancePreferences.defaults,
    PdfMetadataExportMask metadataMask = PdfMetadataExportMask.all,
  }) {
    final cacheKey =
        '${proof.id}_${appearance.showBranding}_${appearance.showCredibilityBlock}_${appearance.showDetailedLocation}_${appearance.includeAllPhotos}_${appearance.reportStyle.name}_${metadataMask.includeClient}_${metadataMask.includeSite}_${metadataMask.includeNote}';
    return _writePdfTempInFlight.putIfAbsent(cacheKey, () async {
      try {
        final bytes = await buildPdfBytes(
          proof,
          labels,
          appearance: appearance,
          metadataMask: metadataMask,
        );
        final dir = await getTemporaryDirectory();
        final file = File('${dir.path}/stampsight_${proof.id}.pdf');
        await file.writeAsBytes(bytes);
        return file;
      } finally {
        Future.microtask(() {
          _writePdfTempInFlight.remove(cacheKey);
        });
      }
    });
  }

