abstract final class ProofExportService {
  static final Map<String, Future<File>> _writePdfTempInFlight = {};

  static const String _pdfFontAsset = 'assets/fonts/Roboto-VF.ttf';

  static pw.Font? _pdfUnicodeFont;

  static String _pdfSafeText(String value) {
    return value
        .replaceAll('\u2014', '-')
        .replaceAll('\u2013', '-')
        .replaceAll('\u2212', '-')
        .replaceAll('\u00A0', ' ')
        .replaceAll('\u202F', ' ')
        .replaceAll('\u00AB', '"')
        .replaceAll('\u00BB', '"')
        .replaceAll('\u2018', "'")
        .replaceAll('\u2019', "'")
        .replaceAll('\u201C', '"')
        .replaceAll('\u201D', '"');
  }

  static Future<pw.Font> _loadPdfUnicodeFont() async {
    if (_pdfUnicodeFont != null) return _pdfUnicodeFont!;
    final data = await rootBundle.load(_pdfFontAsset);
    _pdfUnicodeFont = pw.Font.ttf(data);
    return _pdfUnicodeFont!;
  }

  static const int _pdfImageMaxDim = 800;

  static img.Image _resizeForPdf(img.Image src) {
    if (src.width <= _pdfImageMaxDim && src.height <= _pdfImageMaxDim) {
      return src;
    }
    final landscape = src.width >= src.height;
    return img.copyResize(
      src,
      width: landscape ? _pdfImageMaxDim : null,
      height: landscape ? null : _pdfImageMaxDim,
      interpolation: img.Interpolation.average,
    );
  }

  static String _safeSlug(String raw, {int maxLen = 48}) {
    final s = raw
        .replaceAll(RegExp(r'[^\w\s\-]'), '')
        .replaceAll(RegExp(r'\s+'), '_')
        .trim();
    if (s.isEmpty) return 'preuve';
    return s.length > maxLen ? s.substring(0, maxLen) : s;
  }

  static Future<File> prepareShareableImageFile(Proof proof) async {
    if (proof.refersToLocalImageFile) {
      return File(proof.imagePath);
    }
    final assetPath = ProofCategoryAsset.placeholderFor(proof.proofType);
    ByteData data;
    try {
      data = await rootBundle.load(assetPath);
    } catch (_) {
      data = await rootBundle.load(ProofCategoryAsset.homeMirrorFor(proof.proofType));
    }
    final dir = await getTemporaryDirectory();
    final slug = _safeSlug(proof.title);
    final file = File('${dir.path}/stampsight_${proof.id}_$slug.webp');
    await file.writeAsBytes(data.buffer.asUint8List());
    return file;
  }

  static Future<File> saveImageToExports(Proof proof) async {
    final tmp = await prepareShareableImageFile(proof);
    final exports = await FileUtils.exportsDirectoryPath;
    final slug = _safeSlug(proof.title);
    final ext =
        p.extension(tmp.path).isNotEmpty ? p.extension(tmp.path) : '.jpg';
    final dest = File('$exports/stampsight_${proof.id}_$slug$ext');
    await tmp.copy(dest.path);
    return dest;
  }

  static Future<Uint8List?> _assetAsPngBytes(String assetPath) async {
    try {
      final data = await rootBundle.load(assetPath);
      final decoded = img.decodeImage(data.buffer.asUint8List());
      if (decoded == null) return null;
      return Uint8List.fromList(img.encodePng(decoded));
    } catch (_) {
      return null;
    }
  }

  static String _fmtDt(DateTime dt) {
    String p2(int v) => v.toString().padLeft(2, '0');
    return '${p2(dt.day)}/${p2(dt.month)}/${dt.year}  ${p2(dt.hour)}:${p2(dt.minute)}';
  }

  // ---------------------------------------------------------------------------
  // PDF brand palette (mirrors AppColors for the pdf package)
  // ---------------------------------------------------------------------------
  static final _bleuNuit = PdfColor.fromInt(0xFF0D1B2A);
  static final _grisFonce = PdfColor.fromInt(0xFF1E293B);
  static final _grisLabel = PdfColor.fromInt(0xFF64748B);
  static final _grisClair = PdfColor.fromInt(0xFF94A3B8);
  static final _separateur = PdfColor.fromInt(0xFFE2E8F0);
  static final _fondClair = PdfColor.fromInt(0xFFF8FAFC);

  // ---------------------------------------------------------------------------
  // V3 Premium PDF builder
  // ---------------------------------------------------------------------------
  static Future<Uint8List?> _loadImageAsPng(String filePath) async {
    try {
      final f = File(filePath);
      if (!f.existsSync()) return null;
      final bytes = await f.readAsBytes();
      final decoded = img.decodeImage(bytes);
      if (decoded == null) return null;
      final resized = _resizeForPdf(decoded);
      return Uint8List.fromList(img.encodePng(resized));
    } catch (_) {
      return null;
    }
  }

  static Future<Uint8List> buildPdfBytes(
    Proof proof,
    ProofPdfLabels labels, {
    PdfAppearancePreferences appearance = PdfAppearancePreferences.defaults,
    PdfMetadataExportMask metadataMask = PdfMetadataExportMask.all,
  }) async {
    final font = await _loadPdfUnicodeFont();
    String t(String s) => _pdfSafeText(s);

    // ----- Load proof image (unchanged logic) -----
    Uint8List? png;
    if (proof.refersToLocalImageFile) {
      png = await _loadImageAsPng(proof.imagePath);
    }
    if (png == null) {
      for (final path in [
        ProofCategoryAsset.placeholderFor(proof.proofType),
        ProofCategoryAsset.homeMirrorFor(proof.proofType),
        ProofCategoryAsset.genericProofs,
      ]) {
        png = await _assetAsPngBytes(path);
        if (png != null) break;
      }
    }

    // ----- Load additional images -----
    final List<Uint8List> additionalPngs = [];
    for (final extraPath in proof.additionalImagePaths) {
      final extra = await _loadImageAsPng(extraPath);
      if (extra != null) additionalPngs.add(extra);
    }

    // ----- Computed data -----
    final now = DateTime.now();
    final exportDate = _fmtDt(now);
    final capturedDate = '${t(labels.dateValue)}  ${t(labels.timeValue)}';
    final hasGeo = proof.latitude != null && proof.longitude != null;
    final hasAddress =
        proof.address != null || proof.overlayAddressText != null;
    final refId = proofReferenceLabelFromProofId(proof.id);
    final hasNote =
        proof.description != null && proof.description!.isNotEmpty;
    final hasClient = labels.clientValue != '-';
    final hasSite = labels.siteMissionValue != '-';
    final showClientInPdf = hasClient && metadataMask.includeClient;
    final showSiteInPdf = hasSite && metadataMask.includeSite;
    final showNoteInPdf = hasNote && metadataMask.includeNote;

    final premium = appearance.reportStyle == PdfReportStyle.premium;

    // ----- V3 refined text styles (Premium = hiérarchie forte ; Standard = neutre et dense) -----
    final sBrand = pw.TextStyle(
      font: font,
      fontSize: premium ? 10.5 : 8.5,
      fontWeight: pw.FontWeight.bold,
      color: _bleuNuit,
      letterSpacing: premium ? 3.5 : 2,
    );
    final sTitle = pw.TextStyle(
      font: font,
      fontSize: premium ? 20 : 16,
      fontWeight: pw.FontWeight.bold,
      color: _bleuNuit,
    );
    final sSub = pw.TextStyle(
      font: font,
      fontSize: premium ? 9 : 8,
      color: _grisLabel,
    );
    final sSec = pw.TextStyle(
      font: font,
      fontSize: premium ? 8.5 : 8,
      fontWeight: pw.FontWeight.bold,
      color: premium ? _bleuNuit : _grisFonce,
      letterSpacing: premium ? 1.5 : 0.8,
    );
    final sLbl = pw.TextStyle(font: font, fontSize: 9, color: _grisLabel);
    final sVal = pw.TextStyle(
      font: font, fontSize: 9.5,
      fontWeight: pw.FontWeight.bold, color: _grisFonce,
    );
    final sBody = pw.TextStyle(font: font, fontSize: 9.5, color: _grisFonce);
    final sDiscrete = pw.TextStyle(font: font, fontSize: 8.5, color: _grisLabel);
    final sMeta = pw.TextStyle(font: font, fontSize: 7.5, color: _grisLabel);
    final sFtBrand = pw.TextStyle(
      font: font, fontSize: 8.5,
      fontWeight: pw.FontWeight.bold, color: _bleuNuit,
    );
    final sFtLabel = pw.TextStyle(font: font, fontSize: 8, color: _grisLabel);
    final sFtMeta = pw.TextStyle(font: font, fontSize: 7.5, color: _grisClair);
    final sFtLegal = pw.TextStyle(font: font, fontSize: 7, color: _grisClair);

    // ----- V3 reusable builders -----
    pw.Widget secTitle(String title) => premium
        ? pw.Column(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              pw.Text(t(title).toUpperCase(), style: sSec),
              pw.SizedBox(height: 3),
              pw.Container(width: 28, height: 1.5, color: _bleuNuit),
            ],
          )
        : pw.Padding(
            padding: const pw.EdgeInsets.only(bottom: 2),
            child: pw.Text(t(title).toUpperCase(), style: sSec),
          );

    pw.Widget kv(String label, String value,
            {pw.TextStyle? vs, double lw = 110}) =>
        pw.Padding(
          padding: const pw.EdgeInsets.only(bottom: 4),
          child: pw.Row(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              pw.SizedBox(
                width: lw,
                child: pw.Text(t(label), style: sLbl),
              ),
              pw.Expanded(
                child: pw.Text(t(value), style: vs ?? sVal),
              ),
            ],
          ),
        );

    pw.Widget thinLine() => pw.Container(
          width: double.infinity,
          height: 0.5,
          color: _separateur,
        );

    pw.Widget accentCard({required pw.Widget child}) => premium
        ? pw.Container(
            decoration: pw.BoxDecoration(
              color: _fondClair,
              border: pw.Border(
                left: pw.BorderSide(color: _bleuNuit, width: 2.5),
              ),
            ),
            padding:
                const pw.EdgeInsets.symmetric(horizontal: 12, vertical: 8),
            child: child,
          )
        : pw.Container(
            decoration: pw.BoxDecoration(
              color: PdfColors.white,
              border: pw.Border.all(color: _separateur, width: 0.75),
            ),
            padding:
                const pw.EdgeInsets.symmetric(horizontal: 10, vertical: 7),
            child: child,
          );

    pw.Widget framedPhoto(Uint8List bytes, {required double height}) =>
        pw.Container(
          decoration: pw.BoxDecoration(
            border: pw.Border.all(color: _separateur, width: 0.5),
          ),
          padding: const pw.EdgeInsets.all(3),
          child: pw.Image(
            pw.MemoryImage(bytes),
            height: height,
            fit: pw.BoxFit.contain,
          ),
        );

    final galleryRowH = premium ? 180.0 : 150.0;

    List<pw.Widget> galleryRows(List<Uint8List> photos) {
      final rows = <pw.Widget>[];
      for (var i = 0; i < photos.length; i += 2) {
        if (i > 0) rows.add(pw.SizedBox(height: premium ? 10 : 8));
        final hasSecond = i + 1 < photos.length;
        rows.add(
          pw.Row(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              pw.Expanded(child: framedPhoto(photos[i], height: galleryRowH)),
              pw.SizedBox(width: 10),
              if (hasSecond)
                pw.Expanded(
                    child: framedPhoto(photos[i + 1], height: galleryRowH))
              else
                pw.Expanded(child: pw.SizedBox()),
            ],
          ),
        );
      }
      return rows;
    }

    // ----- Assemble document -----
    final doc = pw.Document(
      title: t(labels.docTitle),
      author: 'StampSight',
    );
    final pdfTheme = pw.ThemeData.withFont(
      base: font, bold: font, italic: font, boldItalic: font,
    );
    final imageBytes = png;

    final allPhotosFull = <Uint8List>[
      if (imageBytes != null) imageBytes,
      ...additionalPngs,
    ];
    final allPhotos = appearance.includeAllPhotos || allPhotosFull.isEmpty
        ? allPhotosFull
        : <Uint8List>[allPhotosFull.first];
    final totalPhotos = allPhotos.length;

    final singlePhotoH = premium ? 370.0 : 300.0;
    final pairPhotoH = premium ? 260.0 : 220.0;
    final heroMultiH = premium ? 280.0 : 230.0;

    doc.addPage(
      pw.MultiPage(
        pageTheme: pw.PageTheme(
          margin: pw.EdgeInsets.symmetric(
            horizontal: premium ? 36 : 32,
            vertical: premium ? 24 : 20,
          ),
          theme: pdfTheme,
        ),
        build: (context) => [
          // ===== HEADER V3 — iconique =====
          pw.Row(
            mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
            crossAxisAlignment: pw.CrossAxisAlignment.end,
            children: [
              pw.Text(
                appearance.showBranding ? 'STAMPSIGHT' : '',
                style: sBrand,
              ),
              pw.Text(
                '${t(labels.referenceLabel)} $refId',
                style: sMeta,
              ),
            ],
          ),
          pw.SizedBox(height: premium ? 5 : 4),
          pw.Container(
            width: double.infinity,
            height: premium ? 2 : 1,
            color: _bleuNuit,
          ),
          pw.SizedBox(height: premium ? 12 : 8),
          pw.Text(t(labels.docTitle), style: sTitle),
          pw.SizedBox(height: 3),
          pw.Row(
            mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
            children: [
              pw.Text(t(labels.subtitle), style: sSub),
              pw.Text(exportDate, style: sMeta),
            ],
          ),
          pw.SizedBox(height: 10),
          thinLine(),
          pw.SizedBox(height: 10),

          // ===== RÉSUMÉ DE PREUVE — grille raffinée =====
          secTitle(labels.proofSummaryTitle),
          pw.SizedBox(height: 8),
          accentCard(
            child: pw.Row(
              crossAxisAlignment: pw.CrossAxisAlignment.start,
              children: [
                pw.Expanded(
                  child: pw.Column(
                    crossAxisAlignment: pw.CrossAxisAlignment.start,
                    children: [
                      kv(labels.typeLabel, labels.typeValue, lw: 85),
                      kv(labels.dateLabel, labels.dateValue, lw: 85),
                      kv(labels.timeLabel, labels.timeValue, lw: 85),
                    ],
                  ),
                ),
                pw.SizedBox(width: 12),
                pw.Expanded(
                  child: pw.Column(
                    crossAxisAlignment: pw.CrossAxisAlignment.start,
                    children: [
                      if (showClientInPdf)
                        kv(labels.clientLabel, labels.clientValue,
                            lw: 85),
                      if (showSiteInPdf)
                        kv(labels.siteMissionLabel,
                            labels.siteMissionValue,
                            lw: 85),
                      if (!showClientInPdf && !showSiteInPdf)
                        kv(labels.contextLabel, labels.contextValue,
                            lw: 85),
                    ],
                  ),
                ),
              ],
            ),
          ),
          pw.SizedBox(height: 10),
          thinLine(),
          pw.SizedBox(height: 10),

          // ===== PHOTOGRAPHIC EVIDENCE — adaptive layout =====
          secTitle(totalPhotos <= 1
              ? labels.photoSectionTitle
              : labels.photoSectionTitlePlural),
          pw.SizedBox(height: 4),
          if (totalPhotos > 1)
            pw.Padding(
              padding: const pw.EdgeInsets.only(bottom: 6),
              child: pw.Text(
                '$totalPhotos photos',
                style: sDiscrete,
              ),
            ),
          // CAS 1 — single photo: full-width hero
          if (totalPhotos == 1)
            pw.Center(child: framedPhoto(allPhotos[0], height: singlePhotoH)),
          // CAS 2 — two photos: balanced side-by-side
          if (totalPhotos == 2)
            pw.Row(
              crossAxisAlignment: pw.CrossAxisAlignment.start,
              children: [
                pw.Expanded(
                    child: framedPhoto(allPhotos[0], height: pairPhotoH)),
                pw.SizedBox(width: 10),
                pw.Expanded(
                    child: framedPhoto(allPhotos[1], height: pairPhotoH)),
              ],
            ),
          // CAS 3+ — hero principal + galerie uniforme
          if (totalPhotos >= 3) ...[
            pw.Center(child: framedPhoto(allPhotos[0], height: heroMultiH)),
            pw.SizedBox(height: 10),
            ...galleryRows(allPhotos.sublist(1)),
          ],
          pw.SizedBox(height: 10),

          // ===== OBSERVATIONS — pull-quote =====
          if (showNoteInPdf) ...[
            secTitle(labels.observationsTitle),
            pw.SizedBox(height: 6),
            pw.Container(
              decoration: pw.BoxDecoration(
                border: pw.Border(
                  left: pw.BorderSide(color: _grisLabel, width: 2),
                ),
              ),
              padding:
                  const pw.EdgeInsets.only(left: 10, top: 2, bottom: 2),
              child: pw.Text(t(labels.noteValue), style: sBody),
            ),
            pw.SizedBox(height: 8),
          ],
          thinLine(),
          pw.SizedBox(height: 8),

          // ===== TRAÇABILITÉ — localisation détaillée ou synthétique =====
          secTitle(labels.traceabilityTitle),
          pw.SizedBox(height: 6),
          if (appearance.showDetailedLocation)
            accentCard(
              child: pw.Column(
                crossAxisAlignment: pw.CrossAxisAlignment.start,
                children: [
                  kv(labels.addressLabel, labels.addressValue, lw: 140),
                  kv(labels.coordinatesLabel, labels.coordinatesValue,
                      vs: sDiscrete, lw: 140),
                  kv(labels.capturedOnLabel, capturedDate, lw: 140),
                  kv(labels.exportedOnLabel, exportDate, lw: 140),
                  kv(labels.sourceLabel, labels.sourceValue, lw: 140),
                  pw.Padding(
                    padding: const pw.EdgeInsets.only(top: 2),
                    child: pw.Row(
                      children: [
                        pw.Text(
                          '${t(labels.geoRecordedLabel)} : '
